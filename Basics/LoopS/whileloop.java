package Basics.LoopS;

public class whileloop {
    public static void main(String[] args) {
        int num = 1 ;
        while (num <= 5) {
            System.out.println(num);
            num+=1;
            
        }

        // do while loop 

        int n = 1 ;
        do {
            System.out.println(n);
            n++;
             
       }
        while(n <= 5);
    }

}
