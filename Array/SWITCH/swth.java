package Array.SWITCH;

import java.util.Scanner;

public class swth {

    public static void main(String[] args) {
            Scanner in = new Scanner(System.in);
            int day = in.nextInt();
            switch (day) {
                case 1 -> System.out.println("monday");
                case 2 -> System.out.println("monday2");
                case 3 -> System.out.println("monday3");
                case 4 -> System.out.println("monday4");
                case 5 -> System.out.println("monday5");
                case 6 -> System.out.println("monday6");
                case 7 -> System.out.println("monday7");
            }
        }
    }
